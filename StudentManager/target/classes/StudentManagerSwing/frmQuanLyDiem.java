    public void getMonHoc() throws SQLException {
        String sql = "Select * from dbo.MONHOC order by MaMH ASC";
        st = cnn.createStatement();
        ResultSet rs = st.executeQuery(sql);
        DefaultTableModel tableModel = (DefaultTableModel) tblQuanLyMonHoc.getModel();
        tableModel.setRowCount(0);
        String MaMH, TenMH, SoTrinh;
        while (rs.next()){
            MaMH = rs.getString(1);
            TenMH = rs.getString(2);
            SoTrinh = rs.getString(3);
            String [] row = {MaMH, TenMH, SoTrinh};
            tableModel.addRow(row);

        }
    }
